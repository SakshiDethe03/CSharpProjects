using System;

namespace DataTypes
{
    class ImplementationAndDemonstrationOfDataTypes
    {
        static void Main(string[] args)
        {
            string name = "Sakshi";

            int age = 18;

            float height = 5.3f;

            char grade = 'A';

            bool isStudent = true;

            Console.WriteLine("Name (string): " + name);
            Console.WriteLine("Age (int): " + age);
            Console.WriteLine("Height (float): " + height);
            Console.WriteLine("Grade (char): " + grade);
            Console.WriteLine("Is Student (bool): " + isStudent);
            Console.WriteLine("\nEnd of the program.");

            Console.ReadKey();
        }
    }
}